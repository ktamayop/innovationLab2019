namespace matching_learning.ml.Domain
{
    /// <summary>
    /// A skill to evaluate.
    /// </summary>
    public class Skill
    {
        /// <summary>
        /// Gets or sets the tag.
        /// </summary>
        /// <value>
        /// The tag.
        /// </value>
        public string Tag { get; set; }

        /// <summary>
        /// Gets or sets the weight.
        /// </summary>
        /// <value>
        /// The weight.
        /// </value>
        public double Weight { get; set; }
    }
}